var climbStairs = function (n) {
	/* 
    #======================================================================================# 
    | 접근 방식 1                                                                          | 
    |--------------------------------------------------------------------------------------|
    | 1 만들기 : 1                                                                         |
    | 2 만들기 : 1 + 1, 2                                                                  |
    | 3 만들기 : 1 + 2, 2 + 1, 1 + 1 + 1                                                   |
    | 4 만들기 : 1 + 3, 3 + 1, 2 + 2 -> 중복제거 -> 1 + 1 + 1 + 1, 1 + 1 + 2, 1 + 2 + 1    |
    |                                           2 + 1 + 1, 2 + 2 (5개)                     |
    |    .                                                                                 |
    |    .                                                                                 |
    | 반복시 (현재 값을 만들 수 있는 가지 수) =                                            |
    |              (이전 값을 만들 수 있는 가지 수) + (이이전 값을 만들 수 있는 가지 수)   |
    #======================================================================================#
    */
	let arr = [1, 1];
	for (let i = 2; i <= n; i++) {
		arr.push(arr[i - 1] + arr[i - 2]);
	}
	return arr[n];
};
