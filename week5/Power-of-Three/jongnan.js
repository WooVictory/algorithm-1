var isPowerOfThree = function (n) {
	/* 
    #===========================================================# 
    | 접근 방식 1                                               | 
    |-----------------------------------------------------------|
    | 3을 곱해가면서 찾기                                       |
    #===========================================================#
    */
	let cur = 1;
	while (cur < n) {
		cur *= 3;
	}
	return cur === n;

	/* 
    #===========================================================# 
    | 솔루션 방식 1                                             | 
    |-----------------------------------------------------------|
    | 3진수로 바꾼 뒤에 확인                                    |
    | 3의 거듭제곱 수라면 맨 앞자리만 1이고 나머지는 0          |
    #===========================================================#
    */
	return n.toString(3).match('^10*$') !== null;

	/* 
    #===========================================================# 
    | 솔루션 방식 2                                             | 
    |-----------------------------------------------------------|
    | int형의 최대 값은 21억 정도이며 3의 거듭제곱이            |
    | 가질 수 있는 최대의 수는 1162261467                       |
    | 따라서 1162261467을 n으로 나누었을 때 나머지 값이 0 이라면|
    | 3의 거듭 제곱이 될 수 있음                                |
    #===========================================================#
    */
	return n > 0 && 1162261467 % n === 0;
};
